import Foundation

class Beverage {
    var brand: String
    var capacity: Int
    var price: Int
    var name: String
    var date: Date
    
    init(brand: String, capacity: Int, price: Int, name: String, date: Date) {
        self.brand = brand
        self.capacity = capacity
        self.price = price
        self.name = name
        self.date = date
    }
}
