import UIKit

class ViewController: UIViewController {
    var vendingMachine = VendingMachine()
    
    override func viewDidLoad() {
        super.viewDidLoad()
        
        let strawberryMileA = Milk(brand: "서울우유",
                                   capacity: 200,
                                   price: 1000,
                                   name: "날마다딸기우유",
                                   milkFlavor: .strawberry)
        
        let strawberryMileB = Milk(brand: "서울우유",
                                   capacity: 200,
                                   price: 1000,
                                   name: "날마다딸기우유",
                                   milkFlavor: .strawberry,
                                   dateOfManufacture: Date() - 30)
        
        let coke = Milk(brand: "펩시",
                        capacity: 350,
                        price: 2000,
                        name: "다이어트콜라",
                        milkFlavor: .strawberry,
                        dateOfManufacture: Date() - 15)
        
        let coffee = Coffee(brand: "맥심",
                            capacity: 400,
                            price: 3000,
                            name: "TOP아메리카노",
                            coffeeBrand: .top,
                            dateOfManufacture: Date() - 50)
        
        vendingMachine.appendBeverage(strawberryMileA)
        vendingMachine.appendBeverage(strawberryMileB)
        vendingMachine.appendBeverage(coke)
        vendingMachine.appendBeverage(coffee)
        
        vendingMachine.printStockBeverages()
    }


}

